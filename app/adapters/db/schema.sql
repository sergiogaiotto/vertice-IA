-- ===== Radar Voz do Cliente — estado por usuário =====
-- user_id é TEXT (não UUID) intencionalmente: no legado SQLite a FK para
-- `users(id)` não era enforced (PRAGMA foreign_keys ficava off), e a
-- coluna acabou usada também como bucket opaco em fluxos de teste/dev.
-- Mantemos TEXT em PG por compat, sem FK — orphan rows são toleráveis.
-- state_json continua TEXT: o frontend envia/recebe a string crua de
